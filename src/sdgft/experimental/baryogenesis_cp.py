def xi_g(
    delta: float = DELTA_F,
    delta_g: float = DELTA_G_F,
    phi: float = PHI,
) -> float:
    """Chiral gravitational coupling: ξ_G = Δ · δ · φ⁻²."""
    return delta * delta_g * phi ** (-2)


XI_G: float = xi_g()
"""ξ_G ≈ 0.00332."""


