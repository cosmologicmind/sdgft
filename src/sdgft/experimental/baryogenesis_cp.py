THETA_MAX_RAD: float = math.pi / 6.0
"""Cone half-opening angle: 30° = π/6."""


def solid_angle_4d_cone(alpha: float) -> float:
    """Solid angle of a cone with half-angle alpha on S³.

    Ω₄(α) = 2π(α − sin(α)cos(α))
    """
    return 2.0 * math.pi * (alpha - math.sin(alpha) * math.cos(alpha))


OMEGA_CONE: float = solid_angle_4d_cone(THETA_MAX_RAD)
"""Solid angle of a single 30° cone on S³."""

OMEGA_S3: float = 2.0 * math.pi ** 2
"""Total surface area of S³."""

CONE_FRACTION: float = OMEGA_CONE / OMEGA_S3
"""Fraction of S³ covered by one cone ≈ 0.00584."""

KAPPA_GEO: float = 6.0 * CONE_FRACTION
"""Geometric efficiency factor: 6 cones × fractional coverage.

κ = 6 · Ω_cone / Ω_{S³} ≈ 0.0351

This represents the probability that a thermal fluctuation at
the sphaleron scale interacts with the chiral cone geometry.
Six cones contribute independently.
"""


