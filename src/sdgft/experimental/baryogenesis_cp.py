@dataclass(frozen=True)
class BaryogenesisResult:
    """Result of a baryogenesis calculation.

    Attributes:
        name: Short identifier.
        label: Human-readable description.
        eta_b: Predicted baryon-to-photon ratio.
        formula: LaTeX formula string.
        reasoning: Physical motivation.
    """
    name: str
    label: str
    eta_b: float
    formula: str
    reasoning: str

    @property
    def deviation_abs(self) -> float:
        return abs(self.eta_b - ETA_B_OBSERVED)

    @property
    def deviation_percent(self) -> float:
        return self.deviation_abs / ETA_B_OBSERVED * 100.0

    @property
    def deviation_sigma(self) -> float:
        return self.deviation_abs / ETA_B_OBSERVED_UNC


# ── Formula A: Pure topology ──────────────────────────────────────

def eta_b_topological(delta_g: float = DELTA_G_F) -> float:
    """η_B = δ⁶(1−δ)/8 — topological, no explicit CP phase.

    This is the existing SDGFT formula from cosmology.py.
    """
    return eta_b(delta_g)


# ── Formula B: Sphaleron mechanism ────────────────────────────────

def eta_b_sphaleron(
    xi_g_val: float | None = None,
    jarlskog: float | None = None,
    t_sph: float = T_SHALERON_GEV,
    m_pl: float = M_PL_GEV,
    g_star: float = G_STAR_EW,
    kappa: float | None = None,
) -> float:
    """η_B from the sphaleron mechanism with SDGFT inputs.

    η_B = (28/79) · κ · ξ_G · J · (T_sph / M_Pl) / g_*

    Physical chain:
    1. Chiral coupling ξ_G generates lepton asymmetry
    2. Jarlskog invariant J provides the CP-violation measure
    3. Sphaleron processes convert η_{B-L} → η_B via factor 28/79
    4. Geometric efficiency κ = 6·Ω_cone/Ω_{S³}
    5. Temperature ratio T_sph/M_Pl sets the scale
    6. Dilution by g_* relativistic species

    Args:
        xi_g_val: Chiral coupling (default: from SDGFT axioms).
        jarlskog: Jarlskog invariant (default: from ckm_phase).
        t_sph: Sphaleron freeze-out temperature in GeV.
        m_pl: Planck mass in GeV.
        g_star: Relativistic degrees of freedom.
        kappa: Geometric efficiency (default: 6·Ω_cone/Ω_{S³}).

    Returns:
        η_B prediction.
    """
    if xi_g_val is None:
        xi_g_val = XI_G
    if jarlskog is None:
        _, jarlskog = _get_ckm_values()
    if kappa is None:
        kappa = KAPPA_GEO

    eta_bl = kappa * xi_g_val * jarlskog * t_sph / m_pl
    return SPHALERON_CONVERSION * eta_bl / g_star


# ── Formula C: Geometric CP ──────────────────────────────────────

def eta_b_geometric_cp(
    delta_g: float = DELTA_G_F,
    delta_cp: float | None = None,
) -> float:
    """η_B = δ⁶(1−δ)/8 · sin(δ_CP) — topology enhanced by CP phase.

    The sin(δ_CP) factor encodes the efficiency of CP violation in
    converting topology into baryon asymmetry.  When δ_CP = π/2 the
    factor is maximal (1.0); for the observed δ_CP ≈ 1.144 rad,
    sin(δ_CP) ≈ 0.910.

    This bridges the gap between pure topology (2.7% deviation) and
    perfect agreement with observation.

    Args:
        delta_g: Lattice tension (1/24).
        delta_cp: CKM CP phase in radians.

    Returns:
        η_B prediction.
    """
    if delta_cp is None:
        delta_cp, _ = _get_ckm_values()
    base = delta_g ** 6 * (1.0 - delta_g) / 8.0
    return base * math.sin(delta_cp)


# ── Formula D: Full chain (κ·ξ_G·sin(δ_CP)·δ⁶/8) ───────────────

def eta_b_full_chain(
    delta_g: float = DELTA_G_F,
    delta: float = DELTA_F,
    phi: float = PHI,
    delta_cp: float | None = None,
) -> float:
    """η_B from the full mechanism chain.

    η_B = (28/79) · (δ⁶/8) · sin(δ_CP) · (1 − δ + ξ_G)

    Combines:
    - δ⁶/8: topological base (6D defect probability / 16-cell dual)
    - sin(δ_CP): CP-violation efficiency
    - (1 − δ + ξ_G): closure + chiral enhancement
    - 28/79: sphaleron conversion

    The correction factor (1 − δ + ξ_G) ≈ 0.962 slightly reduces
    the pure topological result, while sin(δ_CP) ≈ 0.910 reduces
    it further.  The combined effect: 0.962 × 0.910 / (28/79)...

    This formula is an *exploration* of how the different SDGFT
    ingredients combine into a single expression.

    Args:
        delta_g: Lattice tension (1/24).
        delta: Fibonacci-lattice conflict (5/24).
        phi: Golden ratio.
        delta_cp: CKM CP phase in radians.

    Returns:
        η_B prediction.
    """
    if delta_cp is None:
        delta_cp, _ = _get_ckm_values()
    xi = delta * delta_g * phi ** (-2)
    base = delta_g ** 6 / 8.0
    return base * math.sin(delta_cp) * (1.0 - delta_g + xi)


