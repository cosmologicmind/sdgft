# ║  Consistency check: topological ↔ mechanism                     ║
# ╚══════════════════════════════════════════════════════════════════╝

class ConsistencyResult(NamedTuple):
    """Result of the topological ↔ mechanism consistency check."""
    eta_b_topological: float
    eta_b_geometric_cp: float
    eta_b_sphaleron: float
    eta_b_full_chain: float
    kappa_required: float
    kappa_geometric: float
    kappa_ratio: float


def consistency_check(
    delta_g: float = DELTA_G_F,
    delta: float = DELTA_F,
    phi: float = PHI,
) -> ConsistencyResult:
    """Check if the topological and mechanism formulas are consistent.

    Solves for the κ that would be needed to make the sphaleron
    formula match the topological prediction, and compares it to
    the geometric κ = 6·Ω_cone/Ω_{S³}.

    Returns:
        ConsistencyResult with all predictions and κ comparison.
    """
    delta_cp, jarlskog = _get_ckm_values()

    eta_top = eta_b_topological(delta_g)
    eta_geo = eta_b_geometric_cp(delta_g, delta_cp)
    eta_sph = eta_b_sphaleron()
    eta_full = eta_b_full_chain(delta_g, delta, phi, delta_cp)

    # Solve: eta_top = (28/79) · κ_req · ξ_G · J · T_sph/M_Pl / g_*
    xi = xi_g(delta, delta_g, phi)
    numerator = eta_top * G_STAR_EW
    denominator = SPHALERON_CONVERSION * xi * jarlskog * T_SHALERON_GEV / M_PL_GEV
    kappa_required = numerator / denominator if denominator > 0 else float("inf")

    return ConsistencyResult(
        eta_b_topological=eta_top,
        eta_b_geometric_cp=eta_geo,
        eta_b_sphaleron=eta_sph,
        eta_b_full_chain=eta_full,
        kappa_required=kappa_required,
        kappa_geometric=KAPPA_GEO,
        kappa_ratio=kappa_required / KAPPA_GEO if KAPPA_GEO > 0 else float("inf"),
    )


